package com.devpos.hotelapp.models;

public class TextFinalBillModel {
    private String txtShow;
    private String typeText;
    private String key;
    private int pos;

    public TextFinalBillModel() {

    }

    public String getTxtShow() {
        return txtShow;
    }

    public void setTxtShow(String txtShow) {
        this.txtShow = txtShow;
    }

    public String getTypeText() {
        return typeText;
    }

    public void setTypeText(String typeText) {
        this.typeText = typeText;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
